const fs = require('fs').promises;
const path = require('path');
const postgres = require('postgres');

const DB_FILE = path.join(__dirname, 'users.json');
const connectionString = process.env.DATABASE_URL || process.env.POSTGRES_URL || process.env.POSTGRES_PRISMA_URL;

let sql;
let initPromise;

function hasDatabaseConnection() {
    return Boolean(connectionString);
}

function getSql() {
    if (!hasDatabaseConnection()) {
        throw new Error('Database connection string is missing. Set DATABASE_URL or POSTGRES_URL.');
    }

    if (!sql) {
        sql = postgres(connectionString, {
            max: 1,
            idle_timeout: 20,
            connect_timeout: 15,
            prepare: false,
        });
    }

    return sql;
}

async function readLegacyUsers() {
    try {
        const data = await fs.readFile(DB_FILE, 'utf8');
        const users = JSON.parse(data);
        return Array.isArray(users) ? users : [];
    } catch (error) {
        if (error.code === 'ENOENT') {
            return [];
        }

        throw error;
    }
}

async function initializeDatabase() {
    if (!hasDatabaseConnection()) {
        throw new Error('Database connection string is missing. Set DATABASE_URL or POSTGRES_URL.');
    }

    if (!initPromise) {
        initPromise = (async () => {
            const db = getSql();

            await db`
                create table if not exists users (
                    id bigint generated by default as identity primary key,
                    username text not null unique,
                    email text unique,
                    password_hash text not null,
                    created_at timestamptz not null default now()
                )
            `;

            await db`
                create table if not exists password_reset_tokens (
                    token text primary key,
                    user_id bigint not null references users(id) on delete cascade,
                    expires_at timestamptz not null,
                    created_at timestamptz not null default now()
                )
            `;

            const [{ count }] = await db`select count(*)::int as count from users`;

            if (count === 0) {
                const legacyUsers = await readLegacyUsers();

                for (const legacyUser of legacyUsers) {
                    if (!legacyUser?.username || !legacyUser?.password) {
                        continue;
                    }

                    const normalizedEmail = typeof legacyUser.email === 'string' && legacyUser.email.trim()
                        ? legacyUser.email.trim().toLowerCase()
                        : null;

                    await db`
                        insert into users (id, username, email, password_hash)
                        values (${legacyUser.id || null}, ${legacyUser.username}, ${normalizedEmail}, ${legacyUser.password})
                        on conflict (username) do nothing
                    `;
                }
            }
        })();
    }

    return initPromise;
}

async function createUser({ username, email, passwordHash }) {
    await initializeDatabase();
    const db = getSql();

    const [user] = await db`
        insert into users (username, email, password_hash)
        values (${username}, ${email.toLowerCase()}, ${passwordHash})
        returning id, username, email
    `;

    return user;
}

async function findUserByUsername(username) {
    await initializeDatabase();
    const db = getSql();
    const [user] = await db`
        select id, username, email, password_hash
        from users
        where username = ${username}
        limit 1
    `;

    return user || null;
}

async function findUserByEmail(email) {
    if (!email) {
        return null;
    }

    await initializeDatabase();
    const db = getSql();
    const [user] = await db`
        select id, username, email, password_hash
        from users
        where email = ${email.toLowerCase()}
        limit 1
    `;

    return user || null;
}

async function deleteExpiredResetTokens() {
    await initializeDatabase();
    const db = getSql();
    await db`delete from password_reset_tokens where expires_at <= now()`;
}

async function replaceResetToken({ token, userId, expiresAt }) {
    await initializeDatabase();
    const db = getSql();

    await db.begin(async (tx) => {
        await tx`delete from password_reset_tokens where user_id = ${userId}`;
        await tx`
            insert into password_reset_tokens (token, user_id, expires_at)
            values (${token}, ${userId}, ${new Date(expiresAt)})
        `;
    });
}

async function findResetToken(token) {
    await initializeDatabase();
    const db = getSql();

    const [record] = await db`
        select token, user_id, expires_at
        from password_reset_tokens
        where token = ${token}
        limit 1
    `;

    return record || null;
}

async function clearUserResetTokens(userId) {
    await initializeDatabase();
    const db = getSql();
    await db`delete from password_reset_tokens where user_id = ${userId}`;
}

async function updateUserPassword(userId, passwordHash) {
    await initializeDatabase();
    const db = getSql();
    await db`update users set password_hash = ${passwordHash} where id = ${userId}`;
}

module.exports = {
    hasDatabaseConnection,
    initializeDatabase,
    createUser,
    findUserByUsername,
    findUserByEmail,
    deleteExpiredResetTokens,
    replaceResetToken,
    findResetToken,
    clearUserResetTokens,
    updateUserPassword,
};